"""
# Definition for a Node.
class Node:
    def __init__(self, val=None, children=None):
        self.val = val
        self.children = children
"""

class Solution:
    def maxDepth(self, root: 'Node') -> int:
        if not root:
            return 0
        temp=1
        for j in root.children:
            temp=max(temp,1+self.maxDepth(j))
        return temp